	   						<button class="btn btn-primary btn-center" @click="_edit()">Modificar</button>
   						</div>
   					</div>
   				</div>
   			</div>
   		</div>
   </div>
</template>

<script>
	export default {
		template: "#template-password-index",
		data() {
			return {
				form: {
					id: "",
					password: "",
					password2: ""
				}
			}
		},
		methods: {
			_edit() {
				if(this.form.password != this.form.password2 ) {
					swal("", "Las contrase単as deben cohincidir", "info")
					return
				}
				axios.put(`password/${this.id}`, this.form.password)
				.then(resp => {
					swal('',resp.data.text,'success')
				})
				.catch(erro => {
					swal('','Disculpe, ha ocurrido un error','error')
				})
			}
		},
		props: {
			
		}
	}
</script>